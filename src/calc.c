#include <stdio.h>
#include <math.h>
#include "calc.h"

float arrel(double a)
{
    return sqrt(a);
}
float resta(int a, int b)
{
    return a - b;
}
float divisio(double a, double b)
{
    return a / b;
}
float potencia(int a, int b)
{
    return pow(a, b);
}
float suma(int a, int b)
{
    return a + b;
}
float multiplicacio(int a, int b)
{
    return a * b;
}
